uint8_t SYS_CHECKER()
{
	lcd_clear();
	lcd_put_cur(0, 0);
	lcd_send_string("1 >> MANUAL");
	lcd_put_cur(1, 0);
	lcd_send_string("2 >> AUTOMATIC");

	while(COUNTER_CHOICE == 0)
	{
		if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_1) == 1)
		{
			COUNTER_CHOICE = 1;
			break;
		}

		if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_2) == 1)
		{
			COUNTER_CHOICE = 2;
			break;
		}

	}

	return COUNTER_CHOICE;


}


void Critical_PADAL_MANUAL_CONTROL()
{
	/* Case the distance is less than 3 cm in manual control  */

	if(Distance <= CRASH_DIST)
	{
		DC_MOTOR_SetSpeed(0); // Suddenly stop the car
	}

	else
	{
		PEDAL_MANUAL_MOTOR_CONTROL();
	}
}


void MOTOLR_AUTOMATIC_CONTROL()
{

	/*
	 * Automatic controlling the motor speed based ULTRASONIC Readings
	 * */

	lcd_clear();
	lcd_put_cur(0, 0);
	lcd_send_string("Dist:");
	lcd_Send_num(Distance);

	lcd_put_cur(0, 9);
	lcd_send_string("AUTO");

	if(Distance <= CRASH_DIST )
	{
		DC_MOTOR_SetSpeed(0);
	}

	else if(Distance <= 10 && Distance >= 6  )
	{// ULTRASONIC READING IS ZERO
		DC_MOTOR_SetSpeed(150);
	}

	else if(Distance <= 11 && Distance >= 20)
	{// ULTRASONIC READING IS ZERO
		DC_MOTOR_SetSpeed(300);
	}

	else if(Distance <= 21 && Distance >= 41)
	{// ULTRASONIC READING IS ZERO

		DC_MOTOR_SetSpeed(400);
	}

	else
	{ // ULTRASONIC READING IS ZERO
		DC_MOTOR_SetSpeed(600);
	}

}



void PEDAL_MANUAL_MOTOR_CONTROL()
{

	lcd_clear();

	lcd_put_cur(0, 0);
	lcd_send_string("Dist:");
	lcd_Send_num(Distance);

	lcd_put_cur(0, 9);
	lcd_send_string("MANUAL");

	  if(PADAL_X_AXIS_READ() <= 500)
	  {
		  DC_MOTOR_SetSpeed(600);
	  }

	  else if(PADAL_X_AXIS_READ() != 0)
	  {
		  DC_MOTOR_SetSpeed(0);
	  }

	  if(Distance <= CRASH_DIST )
	  {
		  /*
		   * Crash Critical distance
		   * */
		  Critical_PADAL_MANUAL_CONTROL();
	  }

}

uint8_t RESULT;

void APP_START()
{
	  HCSR04_Read();
	  HCSR04_GETREAD();

	  RESULT =  SYS_CHECKER();

	  lcd_put_cur(0, 0);
	  lcd_send_string("Dist:");
	  lcd_put_cur(0, 6);
	  lcd_Send_num(Distance);

	  switch(RESULT)
	  {

	  case 1 : PEDAL_MANUAL_MOTOR_CONTROL();
		  break;
	  case 2 : MOTOLR_AUTOMATIC_CONTROL();
		  break;

	  }

	  lcd_put_cur(1, 0);
	  lcd_Send_num(PADAL_X_AXIS_READ());

	  HAL_Delay(250);
	  lcd_clear();

}


