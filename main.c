void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{

	ICU_CHECK_CALL();

	++ ICU_COUNTER ;

	if( ICU_COUNTER == 1 ) // Rising Edge is detected
	{
		/* 1 >> Clear the Timer Vlaue
		 * 2 >> Set The falling edge ICU
		 * 3 >> Set ICU COUNTER to 2
		 * **/

		__HAL_TIM_SET_COUNTER(htim,0);

		__HAL_TIM_SET_CAPTUREPOLARITY(htim,TIM_CHANNEL_1,TIM_INPUTCHANNELPOLARITY_FALLING);

	}

	else if(ICU_COUNTER == 2)
	{

		/* obtain the counter value
		 * Set Polarity as rising
		 * */

		ICU_VALUE  = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);

		__HAL_TIM_SET_CAPTUREPOLARITY(htim,TIM_CHANNEL_1,TIM_INPUTCHANNELPOLARITY_RISING);

		__HAL_TIM_SET_COUNTER(htim,0);


	}

	else
	{
		ICU_COUNTER = 0;
	}

}

